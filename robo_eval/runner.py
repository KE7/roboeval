def run_eval_sequential(
    suites: List[str],
    vla_url: str,
    results_dir: Path,
    max_episodes: int = 10,
    sim_port: int = 5001,
    no_vlm: bool = False,
    no_think: bool = False,
    vla_name: Optional[str] = None,
    vlm_model: Optional[str] = None,
    vlm_endpoint: Optional[str] = None,
    delta_actions: bool = True,
    num_tasks: int = TASKS_PER_SUITE,
    resume: bool = False,
    vla_urls: Optional[List[str]] = None,
    suites_parallel: Optional[int] = None,
    managed_sim_pool: Optional[SimWorkerPool] = None,
    seed: Optional[int] = None,
    record_video: bool = False,
    record_video_n: int = 3,
    max_workers: int = 10,
    sim_config_path: Optional[Path] = None,
    headless: bool = True,
    task_timeout: int = 21600,
) -> Dict:
    """Run sequential evaluation: one task at a time, shared sim worker.

    This replaces:
    - run_libero_benchmark.sh
    - run_libero_pro_benchmark.sh

    Uses a single sim worker that's reused across tasks.
    Note: suites_parallel and max_workers are accepted but ignored in sequential mode.
    """
    # Generate seed if not provided
    if seed is None:
        seed = random.randint(0, 2**31 - 1)
        logger.info("Generated random seed: %d", seed)

    logs_dir = results_dir / "logs"
    videos_dir = results_dir / "videos"
    logs_dir.mkdir(parents=True, exist_ok=True)
    videos_dir.mkdir(parents=True, exist_ok=True)

    # Save eval config
    save_eval_config(
        results_dir,
        suites=suites,
        episodes_per_task=max_episodes,
        mode="direct" if no_vlm else "planner",
        sim_base_port=sim_port,
        seed=seed,
        sim_workers=1,
    )

    os.environ["VLA_URL"] = vla_url

    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    metadata = {
        "timestamp": timestamp,
        "vla_url": vla_url,
        "episodes_per_task": max_episodes,
        "sim_port": sim_port,
        "mode": "direct (no VLM)" if no_vlm else f"planner ({vlm_model or 'default'})",
        "suites": ", ".join(suites),
        "execution": "sequential",
    }

    logger.info("Starting sequential benchmark at %s", timestamp)
    logger.info("  Results dir: %s", results_dir)
    logger.info("  VLA URL: %s", vla_url)
    logger.info("  Sim URL: http://localhost:%d", sim_port)
    logger.info("  Episodes/task: %d", max_episodes)

    use_managed = managed_sim_pool is not None

    for suite in suites:
        # Reload VLA model if needed (e.g., OpenVLA per-suite checkpoints)
        _maybe_reload_vla(vla_url, suite, vla_name)

        sim_type = get_sim_for_suite(suite)
        # Clamp num_tasks to actual suite size to prevent IndexError
        actual_num_tasks = _clamp_num_tasks(num_tasks, suite)
        logger.info("=== Suite: %s (%d tasks, %d eps each) ===", suite, actual_num_tasks, max_episodes)

        # Use managed pool if provided, otherwise create one
        if use_managed:
            pool = managed_sim_pool
            # Use the first port from the managed pool
            managed_ports = sorted(pool.processes.keys())
            actual_sim_port = managed_ports[0] if managed_ports else sim_port
        else:
            # Warn if the port is already in use (PortAllocator not used in this path)
            if not is_port_available(sim_port):
                logger.warning(
                    "  Port %s may be in use — possible conflict with another robo-eval run",
                    sim_port,
                )
            pool = SimWorkerPool(
                sim_type=sim_type,
                base_port=sim_port,
                num_workers=1,
                headless=headless,
                logs_dir=logs_dir,
            )
            actual_sim_port = sim_port

        try:
            if not use_managed:
                pool.start_all()
                if not pool.wait_all_healthy(timeout=60):
                    logger.warning("  sim worker failed to start on port %d", sim_port)
                    continue

            for task_idx in range(actual_num_tasks):
                log_file = logs_dir / f"{suite}_task{task_idx}.log"

                # Resume: skip completed tasks
                if resume and log_file.exists():
                    text = log_file.read_text()
                    if f"All {max_episodes} episodes complete" in text:
                        logger.info("  Task %d: already complete, skipping", task_idx)
                        continue

                exp_dir = results_dir / "experience" / suite / f"task{task_idx}"
                exp_dir.mkdir(parents=True, exist_ok=True)

                cmd = _build_eval_cmd(
                    task_idx=task_idx,
                    suite=suite,
                    sim_type=sim_type,
                    sim_port=actual_sim_port,
                    max_episodes=max_episodes,
                    experience_dir=exp_dir,
                    no_vlm=no_vlm,
                    no_think=no_think,
                    vlm_model=vlm_model,
                    vlm_endpoint=vlm_endpoint,
                    delta_actions=delta_actions,
                    seed=seed,
                    record_video=record_video,
                    record_video_n=record_video_n,
                    results_dir=results_dir,
                    sim_config_path=sim_config_path,
                    headless=headless,
                )

                env = os.environ.copy()
                env["MUJOCO_GL"] = "egl"
                sim_config = SIM_CONFIGS[sim_type]
                for k, v in sim_config.env_vars.items():
                    env[k] = v

                logger.info("  Task %d ...", task_idx)
                with open(log_file, "w") as lf:
                    proc = subprocess.Popen(
                        cmd,
                        stdout=lf,
                        stderr=subprocess.STDOUT,
                        cwd=str(PROJECT_ROOT),
                        env=env,
                    )
                    try:
                        proc.wait(timeout=task_timeout)
                    except subprocess.TimeoutExpired:
                        logger.warning("  Task %d: TIMEOUT after %.1f hours", task_idx, task_timeout / 3600)
                        proc.kill()
                        proc.wait(timeout=30)

                from .results import count_successes
                n_success, n_total, _ep = count_successes(log_file)
                logger.info("  Task %d: %d/%d", task_idx, n_success, n_total)

                _move_videos(videos_dir)
        finally:
            if not use_managed:
                pool.kill_all()

    # Aggregate
    results = collect_results(results_dir, suites, num_tasks, max_episodes)
    write_scores_json(results, results_dir / "scores.json")
    summary = write_summary(results, results_dir / "summary.txt", metadata)
    logger.info("")
    logger.info(summary)
    logger.info("Done! Results in %s", results_dir)
    return results


def run_native_eval(
    vla_name: str,
    suites: List[str],
    results_dir: Path,
    n_episodes: int = 10,
    task_timeout: int = 21600,
) -> None:
    """Run the internal lerobot-eval baseline path for a VLA.

    This replaces native eval sections in:
    - run_smolvla_50eps_all.sh / run_smolvla_50eps_col2.sh
    - run_full_benchmark.sh steps 4, 9
    - run_openvla_50eps_all.sh phase 5
    """
    if vla_name not in NATIVE_EVAL_CONFIGS:
        logger.error(
            "No native eval config for '%s'. Available: %s",
            vla_name, list(NATIVE_EVAL_CONFIGS.keys()),
        )
        return

    config = NATIVE_EVAL_CONFIGS[vla_name]
    results_dir.mkdir(parents=True, exist_ok=True)

    if config.get("custom"):
        # OpenVLA uses a custom script, not lerobot-eval
        venv_python = str(PROJECT_ROOT / config["venv"] / "bin" / "python")
        script = str(PROJECT_ROOT / config["script"])

        for suite in suites:
            log_file = results_dir / f"{suite}.log"
            logger.info("Running native eval: %s / %s (%d eps)...", vla_name, suite, n_episodes)

            env = os.environ.copy()
            env["MUJOCO_GL"] = "egl"
            env["TRANSFORMERS_OFFLINE"] = "1"
            env["HF_HUB_OFFLINE"] = "1"

            cmd = [
                venv_python, script,
                "--suite", suite,
                "--n-episodes", str(n_episodes),
                "--out-dir", str(results_dir),
            ]

            with open(log_file, "w") as lf:
                proc = subprocess.Popen(
                    cmd,
                    stdout=lf,
                    stderr=subprocess.STDOUT,
                    cwd=str(PROJECT_ROOT),
                    env=env,
                )
                try:
                    proc.wait(timeout=task_timeout)
                except subprocess.TimeoutExpired:
                    logger.warning("  %s: TIMEOUT after %.1f hours, killing", suite, task_timeout / 3600)
                    proc.kill()
                    proc.wait(timeout=30)

            logger.info("  %s: done (check %s)", suite, log_file)
    else:
        # Standard lerobot-eval
        venv_dir = PROJECT_ROOT / config["venv"]
        lerobot_eval = str(venv_dir / "bin" / "lerobot-eval")
        policy_path = config["policy_path"]

        for suite in suites:
            log_file = results_dir / f"{suite}.log"
            logger.info("Running native eval: %s / %s (%d eps)...", vla_name, suite, n_episodes)

            env = os.environ.copy()
            env["MUJOCO_GL"] = "egl"

            cmd = [
                lerobot_eval,
                f"--policy.path={policy_path}",
                "--env.type=libero",
                f"--env.task={suite}",
                "--eval.batch_size=1",
                f"--eval.n_episodes={n_episodes}",
            ]

            with open(log_file, "w") as lf:
                proc = subprocess.Popen(
                    cmd,
                    stdout=lf,
                    stderr=subprocess.STDOUT,
                    cwd=str(PROJECT_ROOT),
                    env=env,
                )
                try:
                    proc.wait(timeout=task_timeout)
                except subprocess.TimeoutExpired:
                    logger.warning("  %s: TIMEOUT after %.1f hours, killing", suite, task_timeout / 3600)
                    proc.kill()
                    proc.wait(timeout=30)

            logger.info("  %s: done (check %s)", suite, log_file)


def _move_videos(videos_dir: Path):
    """Move any stray video files to the videos directory."""
    for pattern in ["demo_videos/*.mp4", "demo_videos/*.avi", "*.mp4", "*.avi"]:
        for vid in PROJECT_ROOT.glob(pattern):
            try:
                vid.rename(videos_dir / vid.name)
            except FileNotFoundError:
                # Another concurrent eval already moved this legacy video.
                continue
            except Exception as e:
                logger.warning("Failed to move video %s: %s", vid, e)
