def _clamp_num_tasks(num_tasks: int, suite: str) -> int:
    """Clamp num_tasks to the actual number of tasks in the suite.

    Prevents IndexError when --max-tasks exceeds the suite size.
    Logs a warning when clamping occurs.

    Args:
        num_tasks: Requested number of tasks.
        suite: Fully-qualified suite name.

    Returns:
        min(num_tasks, actual_suite_size). Falls back to num_tasks if
        the suite size cannot be determined.
    """
    sim_type = get_sim_for_suite(suite)
    try:
        if sim_type == "robocasa":
            from sims.sim_worker import RoboCasaBackend
            suite_size = len(RoboCasaBackend._TASK_INDEX)
        else:
            # All LIBERO, LIBERO-PRO, LIBERO-INF, and RoboTwin suites have 10 tasks
            suite_size = TASKS_PER_SUITE
    except Exception:
        return num_tasks

    if num_tasks > suite_size:
        logger.warning(
            "Requested %d tasks but suite '%s' only has %d tasks — clamping to %d",
            num_tasks, suite, suite_size, suite_size,
        )
        return suite_size
    return num_tasks


def _get_git_commit() -> str:
    """Get the current git commit hash, or 'unknown' on failure."""
    try:
        result = subprocess.run(
            ["git", "rev-parse", "HEAD"],
            capture_output=True, text=True, timeout=5,
            cwd=str(PROJECT_ROOT),
        )
        if result.returncode == 0:
            return result.stdout.strip()
    except Exception:
        pass
    return "unknown"


def save_eval_config(
    results_dir: Path,
    *,
    vla: str = "",
    suites: List[str],
    episodes_per_task: int,
    max_steps: Optional[int] = None,
    mode: str = "direct",
    sim_base_port: int = 5300,
    seed: int = 0,
    vla_replicas: int = 1,
    sim_workers: int = 10,
    command: str = "",
) -> Path:
    """Write eval_config.yaml to the results directory using yaml.safe_dump."""
    timestamp = datetime.now().strftime("%Y-%m-%dT%H:%M:%S")
    git_commit = _get_git_commit()

    # Use yaml.safe_dump instead of manual f-string formatting to avoid
    # YAML injection from the command field.
    config_data = {
        "timestamp": timestamp,
        "vla": vla,
        "suites": list(suites),
        "episodes_per_task": episodes_per_task,
        "max_steps": max_steps if max_steps is not None else "auto",
        "mode": mode,
        "sim_base_port": sim_base_port,
        "seed": seed,
        "vla_replicas": vla_replicas,
        "sim_workers": sim_workers,
        "git_commit": git_commit,
        "command": command,
    }

    content = "# Eval configuration - generated by robo-eval\n" + yaml.safe_dump(
        config_data, default_flow_style=False, sort_keys=False
    )

    config_path = results_dir / "eval_config.yaml"
    config_path.write_text(content)
    logger.info("Config saved to %s", config_path)
    return config_path


def _build_eval_cmd(
    task_idx: int,
    suite: str,
    sim_type: str,
    sim_port: int,
    max_episodes: int,
    experience_dir: Path,
    no_vlm: bool = False,
    no_think: bool = False,
    vlm_model: Optional[str] = None,
    vlm_endpoint: Optional[str] = None,
    delta_actions: bool = True,
    max_steps: Optional[int] = None,
    seed: Optional[int] = None,
    record_video: bool = False,
    record_video_n: int = 3,
    results_dir: Optional[Path] = None,
    sim_config_path: Optional[Path] = None,
    headless: bool = True,
) -> List[str]:
    """Build the run_sim_eval.py eval command.

    If max_steps is not provided, uses suite-specific defaults from config.
    """
    # Use suite-specific max_steps if not explicitly overridden
    if max_steps is None:
        max_steps = get_suite_max_steps(suite)

    cmd = [
        str(EVAL_PYTHON),
        str(EVAL_SCRIPT),
        "eval",
        "--sim", sim_type,
        "--task", str(task_idx),
        "--suite", suite,
        "--sim-url", f"http://localhost:{sim_port}",
        "--max-episodes", str(max_episodes),
        "--max-steps", str(max_steps),
        "--experience-dir", str(experience_dir),
    ]
    if headless:
        cmd.append("--headless")

    if delta_actions:
        cmd.append("--delta-actions")
    if no_vlm:
        cmd.append("--no-vlm")
    if no_think:
        cmd.append("--no-think")
    if vlm_model:
        cmd.extend(["--vlm-model", vlm_model])
    if vlm_endpoint:
        cmd.extend(["--vlm-endpoint", vlm_endpoint])
    if seed is not None:
        cmd.extend(["--seed", str(seed)])
    if results_dir is not None:
        cmd.extend(["--results-dir", str(results_dir)])
    if record_video:
        cmd.append("--record-video")
        cmd.extend(["--record-video-n", str(record_video_n)])
    if sim_config_path is not None:
        cmd.extend(["--sim-config", str(sim_config_path)])

    return cmd


def _maybe_reload_vla(vla_url: str, suite: str, vla_name: Optional[str]) -> None:
    """If the VLA needs a different model for this suite, send a /reload request.

    Currently only OpenVLA requires per-suite model swaps (separate fine-tuned
    checkpoints per LIBERO suite).  Other VLAs (pi0.5, SmolVLA) use a single
    checkpoint for all suites and this function is a no-op for them.

    Args:
        vla_url: URL of the VLA proxy (or direct VLA server).
        suite: Fully-qualified suite name (e.g. "libero_object").
        vla_name: Short VLA name (e.g. "openvla").  If None, no reload is attempted.
    """
    if vla_name != "openvla":
        return

    override = get_openvla_model_for_suite(suite)
    if override is None:
        logger.debug("No OpenVLA model override for suite %s, using default", suite)
        return

    model_id = override["model_id"]
    unnorm_key = override["unnorm_key"]

    logger.info(
        "[vla-reload] Requesting model swap for suite %s: model=%s, unnorm_key=%s",
        suite, model_id, unnorm_key,
    )

    try:
        resp = _requests.post(
            f"{vla_url}/reload",
            json={"model_id": model_id, "unnorm_key": unnorm_key},
            timeout=600,  # Model loading can take several minutes
        )
        data = resp.json()
        if data.get("success"):
            reloaded = data.get("reloaded", False)
            if reloaded:
                logger.info("[vla-reload] Model swapped successfully to %s", model_id)
            else:
                logger.info("[vla-reload] Model %s already loaded, no swap needed", model_id)
        else:
            error = data.get("error", "unknown error")
            logger.error("[vla-reload] Model swap FAILED: %s", error)
            raise RuntimeError(f"VLA model reload failed for suite {suite}: {error}")
    except _requests.RequestException as e:
        logger.error("[vla-reload] HTTP error during model reload: %s", e)
        raise RuntimeError(f"VLA model reload failed for suite {suite}: {e}") from e


def run_eval_parallel(
    suites: List[str],
    vla_url: str,
    results_dir: Path,
    max_episodes: int = 10,
    sim_base_port: int = 5300,
    no_vlm: bool = False,
    no_think: bool = False,
    vla_name: Optional[str] = None,
    vlm_model: Optional[str] = None,
    vlm_endpoint: Optional[str] = None,
    delta_actions: bool = True,
    num_tasks: int = TASKS_PER_SUITE,
    resume: bool = False,
    vla_urls: Optional[List[str]] = None,
    suites_parallel: Optional[int] = None,
    managed_sim_pool: Optional["SimWorkerPool"] = None,
    seed: Optional[int] = None,
    record_video: bool = False,
    record_video_n: int = 3,
    max_workers: int = 10,
    sim_config_path: Optional[Path] = None,
    headless: bool = True,
    task_timeout: int = 21600,
) -> Dict:
    """Run parallel evaluation: all tasks in a suite concurrently.

    When multiple VLA servers are provided via vla_urls, tasks are distributed
    across them round-robin. When suites_parallel > 1, multiple suites run
    concurrently.

    This is the main orchestration function that replaces:
    - run_libero_benchmark_parallel.sh
    - run_libero_pro_benchmark_parallel.sh
    - run_single_suite.sh

    Args:
        suites: List of suite names to evaluate
        vla_url: Primary URL of the VLA policy server (or proxy URL)
        results_dir: Where to write results
        max_episodes: Episodes per task
        sim_base_port: Base port for sim workers
        no_vlm: Skip VLM planning (direct mode)
        no_think: Disable VLM thinking tokens
        vlm_model: Override VLM model name
        vlm_endpoint: Override VLM proxy endpoint
        delta_actions: Use delta actions (default True)
        num_tasks: Tasks per suite (default 10)
        resume: Skip tasks with existing complete logs
        vla_urls: List of VLA server URLs for round-robin distribution
        suites_parallel: Number of suites to run in parallel (None = sequential)
        managed_sim_pool: Pre-started SimWorkerPool from StackManager.
            When provided, sim workers are already running and should NOT
            be killed per-task (the StackManager handles cleanup).
        max_workers: Maximum concurrent eval processes per suite (from --tasks-parallel).

    Returns:
        Aggregated results dict.
    """
    # Use single VLA URL if no list provided
    if not vla_urls:
        vla_urls = [vla_url]

    # Generate seed if not provided
    if seed is None:
        seed = random.randint(0, 2**31 - 1)
        logger.info("Generated random seed: %d", seed)

    logs_dir = results_dir / "logs"
    videos_dir = results_dir / "videos"
    logs_dir.mkdir(parents=True, exist_ok=True)
    videos_dir.mkdir(parents=True, exist_ok=True)

    # Save eval config
    save_eval_config(
        results_dir,
        suites=suites,
        episodes_per_task=max_episodes,
        mode="direct" if no_vlm else "planner",
        sim_base_port=sim_base_port,
        seed=seed,
        vla_replicas=len(vla_urls),
        sim_workers=(
            len(managed_sim_pool.processes)
            if managed_sim_pool is not None
            else min(max_workers, num_tasks)
        ),
    )

    # Set VLA_URL for eval processes (primary URL)
    os.environ["VLA_URL"] = vla_url

    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    metadata = {
        "timestamp": timestamp,
        "vla_url": vla_url,
        "vla_servers": len(vla_urls),
        "episodes_per_task": max_episodes,
        "sim_base_port": sim_base_port,
        "mode": "direct (no VLM)" if no_vlm else f"planner ({vlm_model or 'default'})",
        "suites": ", ".join(suites),
    }

    logger.info("Starting benchmark at %s", timestamp)
    logger.info("  Results dir: %s", results_dir)
    logger.info("  VLA URLs: %s", ", ".join(vla_urls))
    logger.info("  Episodes/task: %d", max_episodes)
    logger.info("  Suites: %s", ", ".join(suites))
    logger.info("  Mode: %s", "direct" if no_vlm else "planner")
    if len(vla_urls) > 1:
        logger.info("  VLA servers: %d (round-robin)", len(vla_urls))

    workers_per_suite = min(max_workers, num_tasks)
    concurrent_suites = (
        min(suites_parallel, len(suites))
        if suites_parallel and suites_parallel > 1 and len(suites) > 1
        else 1
    )

    # Validate sim worker count for parallel suites
    if managed_sim_pool is not None:
        total_tasks_parallel = concurrent_suites * workers_per_suite
        available_workers = len(managed_sim_pool.processes)
        if available_workers < total_tasks_parallel:
            raise ValueError(
                f"Not enough sim workers for parallel suites: "
                f"{concurrent_suites} suites × {workers_per_suite} workers = "
                f"{total_tasks_parallel} needed, but only "
                f"{available_workers} sim workers available."
            )

    # If suites_parallel is set, run suites concurrently using threads
    if suites_parallel and suites_parallel > 1 and len(suites) > 1:
        import concurrent.futures

        actual_parallel = min(suites_parallel, len(suites))
        logger.info("Running %d suites with %d in parallel", len(suites), actual_parallel)

        def _run_suite(suite_name: str, slot_idx: int):
            # Reload VLA model if needed (e.g., OpenVLA per-suite checkpoints)
            _maybe_reload_vla(vla_url, suite_name, vla_name)
            suite_base_port = sim_base_port + slot_idx * num_tasks
            managed_ports = None
            if managed_sim_pool is not None:
                all_ports = sorted(managed_sim_pool.processes.keys())
                start = slot_idx * workers_per_suite
                managed_ports = all_ports[start:start + workers_per_suite]
            return _run_single_suite_parallel(
                suite=suite_name,
                vla_urls=vla_urls,
                results_dir=results_dir,
                logs_dir=logs_dir,
                max_episodes=max_episodes,
                sim_base_port=suite_base_port,
                no_vlm=no_vlm,
                no_think=no_think,
                vlm_model=vlm_model,
                vlm_endpoint=vlm_endpoint,
                delta_actions=delta_actions,
                num_tasks=num_tasks,
                resume=resume,
                managed_sim_pool=managed_sim_pool,
                seed=seed,
                record_video=record_video,
                record_video_n=record_video_n,
                max_workers=max_workers,
                managed_ports=managed_ports,
                sim_config_path=sim_config_path,
                headless=headless,
                task_timeout=task_timeout,
            )

        for batch_start in range(0, len(suites), actual_parallel):
            batch = suites[batch_start:batch_start + actual_parallel]
            with concurrent.futures.ThreadPoolExecutor(max_workers=actual_parallel) as executor:
                futures = {
                    executor.submit(_run_suite, suite_name, slot_idx): suite_name
                    for slot_idx, suite_name in enumerate(batch)
                }
                for future in concurrent.futures.as_completed(futures):
                    suite_name = futures[future]
                    try:
                        future.result()
                    except Exception as e:
                        logger.error("ERROR in suite %s: %s", suite_name, e)
    else:
        # Sequential suite execution (default)
        _run_suites_sequential(
            suites=suites,
            vla_urls=vla_urls,
            results_dir=results_dir,
            logs_dir=logs_dir,
            max_episodes=max_episodes,
            sim_base_port=sim_base_port,
            no_vlm=no_vlm,
            no_think=no_think,
            vlm_model=vlm_model,
            vlm_endpoint=vlm_endpoint,
            delta_actions=delta_actions,
            num_tasks=num_tasks,
            resume=resume,
            managed_sim_pool=managed_sim_pool,
            seed=seed,
            record_video=record_video,
            record_video_n=record_video_n,
            max_workers=max_workers,
            sim_config_path=sim_config_path,
            headless=headless,
            task_timeout=task_timeout,
            vla_name=vla_name,
        )

    # 4) Aggregate and write results
    results = collect_results(results_dir, suites, num_tasks, max_episodes)
    write_scores_json(results, results_dir / "scores.json")
    summary = write_summary(results, results_dir / "summary.txt", metadata)
    logger.info("")
    logger.info(summary)
    logger.info("Done! Results in %s", results_dir)

    return results


def _run_single_suite_parallel(
    suite: str,
    vla_urls: List[str],
    results_dir: Path,
    logs_dir: Path,
    max_episodes: int,
    sim_base_port: int,
    no_vlm: bool,
    no_think: bool,
    vlm_model: Optional[str],
    vlm_endpoint: Optional[str],
    delta_actions: bool,
    num_tasks: int,
    resume: bool,
    managed_sim_pool: Optional[SimWorkerPool] = None,
    seed: Optional[int] = None,
    record_video: bool = False,
    record_video_n: int = 3,
    max_workers: int = 10,
    managed_ports: Optional[List[int]] = None,
    sim_config_path: Optional[Path] = None,
    headless: bool = True,
    task_timeout: int = 21600,
) -> None:
    """Run a single suite with parallel tasks.

    max_workers limits concurrent eval processes (wired from --tasks-parallel).
    Uses a polling loop (not sequential proc.wait()) so we can enforce the
    concurrency cap while tasks finish at different times.

    When managed_sim_pool is provided, sim workers are already running
    (managed by StackManager) and should not be started/killed here.
    """
    sim_type = get_sim_for_suite(suite)

    # Clamp num_tasks to actual suite size to prevent IndexError
    actual_num_tasks = _clamp_num_tasks(num_tasks, suite)

    logger.info("=== Suite: %s (%d tasks, %d eps each) ===", suite, actual_num_tasks, max_episodes)

    # Check which tasks need to run (for resume mode)
    tasks_to_run = list(range(actual_num_tasks))
    if resume:
        tasks_to_run = []
        for t in range(actual_num_tasks):
            log_file = logs_dir / f"{suite}_task{t}.log"
            if log_file.exists():
                text = log_file.read_text()
                if f"All {max_episodes} episodes complete" in text:
                    logger.info("  Task %d: already complete, skipping", t)
                    continue
            tasks_to_run.append(t)

        if not tasks_to_run:
            logger.info("  All tasks complete for %s, skipping", suite)
            return

    n_workers = len(tasks_to_run)

    # Use managed pool if provided, otherwise create one
    use_managed = managed_sim_pool is not None
    if use_managed:
        pool = managed_sim_pool
        port_count = len(managed_ports) if managed_ports is not None else len(pool.processes)
        logger.info("  Using %d pre-started sim workers", port_count)
    else:
        # Warn if any ports are already in use (PortAllocator not used in this path)
        used_ports = [
            sim_base_port + i for i in range(n_workers)
            if not is_port_available(sim_base_port + i)
        ]
        if used_ports:
            logger.warning(
                "  Port(s) %s may be in use — possible conflict with another robo-eval run",
                used_ports,
            )
        pool = SimWorkerPool(
            sim_type=sim_type,
            base_port=sim_base_port,
            num_workers=n_workers,
            headless=headless,
            logs_dir=logs_dir,
        )

    # Track active processes for cleanup in finally block
    active_procs: Dict[int, Tuple[subprocess.Popen, int, float]] = {}
    # task_idx -> (proc, original_index, start_time)

    try:
        if not use_managed:
            ports = pool.start_all()
            logger.info(
                "  Starting %d sim workers on ports %d-%d...",
                n_workers, sim_base_port, sim_base_port + n_workers - 1,
            )

            if not pool.wait_all_healthy(timeout=60):
                logger.warning("  Some sim workers failed to start")

            logger.info("  All sim workers ready")

        # Determine which ports to use for each task
        if use_managed:
            available_ports = managed_ports or sorted(pool.processes.keys())
        else:
            available_ports = [sim_base_port + i for i in range(n_workers)]

        required_ports = min(max_workers, len(tasks_to_run))
        if len(available_ports) < required_ports:
            raise ValueError(
                f"Suite {suite} requires {required_ports} sim workers for "
                f"--tasks-parallel {max_workers}, but only {len(available_ports)} were allocated."
            )

        # Launch with concurrency limit and polling loop (see docstring above).
        from .results import count_successes

        pending = list(enumerate(tasks_to_run))  # (original_index, task_idx)
        completed_count = 0

        def _launch_task(i: int, task_idx: int):
            port = available_ports[i % len(available_ports)]
            log_file = logs_dir / f"{suite}_task{task_idx}.log"
            exp_dir = results_dir / "experience" / suite / f"task{task_idx}"
            exp_dir.mkdir(parents=True, exist_ok=True)

            assigned_vla_url = vla_urls[i % len(vla_urls)]

            cmd = _build_eval_cmd(
                task_idx=task_idx,
                suite=suite,
                sim_type=sim_type,
                sim_port=port,
                max_episodes=max_episodes,
                experience_dir=exp_dir,
                no_vlm=no_vlm,
                no_think=no_think,
                vlm_model=vlm_model,
                vlm_endpoint=vlm_endpoint,
                delta_actions=delta_actions,
                seed=seed,
                record_video=record_video,
                record_video_n=record_video_n,
                results_dir=results_dir,
                sim_config_path=sim_config_path,
                headless=headless,
            )

            env = os.environ.copy()
            env["MUJOCO_GL"] = "egl"
            env["VLA_URL"] = assigned_vla_url
            sim_config = SIM_CONFIGS[sim_type]
            for k, v in sim_config.env_vars.items():
                env[k] = v

            with open(log_file, "w") as lf:
                proc = subprocess.Popen(
                    cmd,
                    stdout=lf,
                    stderr=subprocess.STDOUT,
                    cwd=str(PROJECT_ROOT),
                    env=env,
                )
            active_procs[task_idx] = (proc, i, time.time())

        # Launch initial batch (up to max_workers)
        initial_batch = min(max_workers, len(pending))
        for _ in range(initial_batch):
            i, task_idx = pending.pop(0)
            _launch_task(i, task_idx)

        logger.info(
            "  Launched %d eval processes (max %d concurrent)",
            initial_batch, max_workers,
        )

        # Poll for completion and launch new tasks as slots open
        while active_procs:
            for task_idx in list(active_procs.keys()):
                proc, i, start_time = active_procs[task_idx]
                ret = proc.poll()

                if ret is not None:
                    # Process completed
                    del active_procs[task_idx]
                    completed_count += 1

                    # Kill sim worker if we're managing our own pool
                    if not use_managed:
                        port = available_ports[i % len(available_ports)]
                        pool.kill_worker(port)

                    # Report result
                    log_file = logs_dir / f"{suite}_task{task_idx}.log"
                    n_success, n_total, _ep = count_successes(log_file)
                    logger.info(
                        "  Task %d: %d/%d successes (%d/%d tasks done)",
                        task_idx, n_success, n_total,
                        completed_count, len(tasks_to_run),
                    )

                    _move_videos(results_dir / "videos")

                    # Launch next pending task if available
                    if pending:
                        next_i, next_task = pending.pop(0)
                        _launch_task(next_i, next_task)

                elif time.time() - start_time > task_timeout:
                    # Timeout after task_timeout seconds
                    logger.warning(
                        "  Task %d: TIMEOUT after %.1f hours, killing process",
                        task_idx, task_timeout / 3600,
                    )
                    proc.kill()
                    proc.wait(timeout=30)
                    del active_procs[task_idx]
                    completed_count += 1

                    if not use_managed:
                        port = available_ports[i % len(available_ports)]
                        pool.kill_worker(port)

                    # Launch next pending task if available
                    if pending:
                        next_i, next_task = pending.pop(0)
                        _launch_task(next_i, next_task)

            time.sleep(1)  # Brief poll interval

    finally:
        # Kill any remaining active eval processes on interruption
        if active_procs:
            logger.warning("Cleaning up %d active eval processes...", len(active_procs))
        for task_idx, (proc, _, _) in list(active_procs.items()):
            try:
                proc.kill()
                proc.wait(timeout=10)
            except Exception as e:
                logger.debug("Failed to kill task %d process: %s", task_idx, e)

        # Only kill sim workers if we created them
        if not use_managed:
            pool.kill_all()


